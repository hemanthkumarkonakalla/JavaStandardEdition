package com.java.standard.edition.math;

public class MathDemo 
{
	public static void main(String[] args) 
	{
		System.out.println(Math.max(10, 20));//20
		System.out.println(Math.min(23, 87));//23
		System.out.println(Math.ceil(67.1));//68.0
		System.out.println(Math.floor(67.9));//67.0
		System.out.println(Math.round(56.6));//57
		System.out.println(Math.random());//some random value
		System.out.println(Math.sqrt(25));//5.0
		System.out.println(Math.abs(-100));//100
		
	}

}

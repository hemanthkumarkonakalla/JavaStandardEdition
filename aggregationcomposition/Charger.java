package com.java.standard.edition.aggregationcomposition;

public class Charger
{
	String color;
	int cost;
	
	public Charger()
	{
		
	}
	public Charger(String color,int cost)
	{
		this.color=color;
		this.cost=cost;
	}

	public String getColor() {
		return color;
	}

	public int getCost() {
		return cost;
	}
	
	

}
